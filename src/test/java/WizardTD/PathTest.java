    @Test
    public void testBoundaryConditions() {
        // Test when multiple paths exists
        int[][] grid = {
            {0, 0, 0},
            {0, 0, 0},
            {0, 0, 0}
        };

        Path pathFinder = new Path();
        Path.Node start = pathFinder.new Node(0, 0); // Top-left corner
        Path.Node goal = pathFinder.new Node(2, 2); // Bottom-right corner
        List<Path.Node> path = pathFinder.findPath(grid, start, goal);

        assertNotNull(path, "Path should not be null");
        assertFalse(path.isEmpty(), "Path should not be empty");
    }
